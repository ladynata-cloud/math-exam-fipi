function taskWhole(){
 let t,p;
 do{t=pick([25,30,40,45,50,60,75,80,120,150,180,200,240]);p=pick([6,7,9,12,14,18,21,24,27,33])}
 while(!(p<t && t-p!==p && t!==100 && p!==100 && t-p!==100));
 const [story,u]=pick(SCENES)(t,p);
 const opts=[
  {t:p+' '+u,w:'Это выделенная часть, а не целое. За 100% берут всё количество, из которого её выделили.'},
  {t:t+' '+u,w:null},
  {t:(t-p)+' '+u,w:'Это оставшаяся часть. 100% — всё вместе: и она, и выделенная часть.'},
  {t:'100 '+u,w:'Число 100 в условии не встречается. 100% — это всё целое, каким бы числом оно ни выражалось.'}
 ];
 const mix=shuffle(opts);
 return {type:'choice',prompt:story+' <b>Что здесь принято за 100%?</b>',
  options:mix.map(o=>o.t),answer:mix.findIndex(o=>!o.w),
  wrong:Object.fromEntries(mix.map((o,i)=>[i,o.w]).filter(x=>x[1])),
  hint1:'Спросите себя: часть <b>чего</b> мы выделяем? Вот это «чего» и есть 100%.',
  hint2:'Целое всегда больше своей части. Из четырёх чисел выберите то, из которого взяли остальные.',
  solution:'Часть ('+p+') выделили из всего количества ('+t+'). Значит 100% — это <b>'+t+' '+u+'</b>, а '+p+' — только доля от них.'};
}

function taskOne(){
 const n=pick([150,200,250,300,350,400,450,500,600,700,800,900,1200,1500,2400]);
 const a=n/100;
 const v=rnd(0,2);
 const prompt=v===0?'Найдите 1% от числа <b>'+n+'</b>.'
  :v===1?'В городе <b>'+n+'</b> тысяч жителей. Сколько тысяч составляет 1%?'
  :'Один процент от числа <b>'+n+'</b> — чему он равен?';
 return {type:'input',prompt:prompt,answer:a,
  traps:[{v:n/10,msg:'Вы нашли 10%, а не 1%. Один процент в десять раз меньше: делить надо на 100, а не на 10.'},
         {v:n*100,msg:'Вы умножили на 100. Один процент — это сотая <b>часть</b>, поэтому число делят.'},
         {v:n/2,msg:'Это половина, то есть 50%. Один процент — это одна сотая.'},
         {v:100,msg:'Сто — это количество процентов в целом, а не значение одного процента.'}],
  hint1:'Целое разделили на сто равных частей. Один процент — одна такая часть.',
  hint2:'Разделите '+n+' на 100.',
  solution:'1% = '+n+' : 100 = <b>'+fmt(a)+'</b>. Проверка: сто таких частей дают '+n+'.'};
}

function taskShare(){
 let tot,p,part;
 do{tot=pick([20,40,50,60,80,100,120,140,160,180,200,240,300,400]);
    p=pick([5,10,15,20,25,30,35,40,45,50,60,70,75,80]);part=tot*p/100}
 while(!(Number.isInteger(part) && part>0 && p!==100 && part!==p && tot!==p));
 const v=rnd(0,2);
 const prompt=v===0?'Из <b>'+tot+'</b> предметов выбрали <b>'+part+'</b>. Сколько это процентов?'
  :v===1?'В классе <b>'+tot+'</b> '+plural(tot,'ученик','ученика','учеников')+', на олимпиаду поехали <b>'+part+'</b>. Какой это процент?'
  :'Какую часть в процентах составляет <b>'+part+'</b> от <b>'+tot+'</b>?';
 return {type:'input',prompt:prompt,answer:p,unit:'%',
  traps:[{v:part,msg:'Это сама часть в штуках, а не в процентах. Её ещё нужно сравнить с целым.'},
         {v:100-p,msg:'Вы посчитали процент оставшихся, а спрашивают про выделенную часть.'},
         {v:r2(tot/part*100),msg:'Дробь перевёрнута: сверху должна стоять часть ('+part+'), снизу целое ('+tot+').'},
         {v:r2(tot-part),msg:'Это остаток в штуках. Нужен процент от целого.'}],
  hint1:'Сначала узнайте, какую долю часть составляет от целого: '+part+' : '+tot+'.',
  hint2:'Полученную долю умножьте на 100 — доля превратится в проценты.',
  solution:part+' : '+tot+' = '+fmt(part/tot)+'. Умножаем на 100: <b>'+p+'%</b>.'};
}

function makeTask(skill){return {whole:taskWhole,one:taskOne,share:taskShare}[skill]()}

