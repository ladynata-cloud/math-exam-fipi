function taskSolve(){
 let a,b,k,c,d;
 do{a=rnd(2,12);b=rnd(2,12);k=rnd(2,9);c=a*k;d=b*k}
 while(!(a!==b && c!==a && d!==b && a*d===b*c));
 const pos=rnd(0,3);
 const val=[a,b,c,d][pos];
 const shownVals=[a,b,c,d].map((x,i)=>i===pos?'<b>x</b>':String(x));
 const prompt='Найдите неизвестный член пропорции:<br><span style="font-size:20px">'
  +shownVals[0]+' : '+shownVals[1]+' = '+shownVals[2]+' : '+shownVals[3]+'</span>';
 const traps=[];
 if(pos===2){traps.push({v:r2(a*b/d),msg:'Перемножены не те члены. Крайние здесь — '+a+' и '+d+', средние — <b>x</b> и '+b+'.'});
             traps.push({v:r2(b*d/a),msg:'Проверьте, что на что делите: x = '+a+' · '+d+' : '+b+'.'})}
 if(pos===3){traps.push({v:r2(a*c/b),msg:'Перемножены не те члены: крайние — '+a+' и <b>x</b>, средние — '+b+' и '+c+'.'});
             traps.push({v:r2(a*b/c),msg:'Проверьте порядок: x = '+b+' · '+c+' : '+a+'.'})}
 if(pos===0)traps.push({v:r2(c*d/b),msg:'Крайние члены — <b>x</b> и '+d+', средние — '+b+' и '+c+'. Значит x · '+d+' = '+b+' · '+c+'.'});
 if(pos===1)traps.push({v:r2(a*c/d),msg:'Средние члены — <b>x</b> и '+c+', крайние — '+a+' и '+d+'. Значит '+a+' · '+d+' = <b>x</b> · '+c+'.'});
 traps.push({v:r2(val+k),msg:'Пропорция решается умножением и делением, а не прибавлением.'});
 return {type:'input',prompt:prompt,answer:val,traps:traps,
  hint1:'Крайние члены — первый и последний, средние — два в середине. Их произведения равны.',
  hint2:'Запишите равенство '+a+' · '+d+' = '+b+' · '+c+' и выразите из него <b>x</b>.',
  solution:'Произведение крайних равно произведению средних: '+a+' · '+d+' = '+b+' · '+c+' ('+(a*d)+' = '+(b*c)+').<br>'
   +'Отсюда <b>x = '+val+'</b>. Проверка: обе части дают '+(a*d)+'.'};
}

