function taskWhich(){
 const right=pick(Object.keys(STORIES));
 const story=pick(STORIES[right])();
 const mix=shuffle(Object.keys(KIND));
 return {type:'choice',prompt:story+'<br><b>Какой это вопрос?</b>',
  options:mix.map(k=>KIND[k]),answer:mix.indexOf(right),
  wrong:Object.fromEntries(mix.map((k,i)=>[i,NOT[k]]).filter(x=>mix[x[0]]!==right)),
  hint1:'Выпишите, какие числа даны. Целое? Часть? Процент? Два значения одной величины?',
  hint2:'То, чего в условии нет, и есть ответ на вопрос задачи.',
  solution:'В условии не хватает как раз этого: <b>'+KIND[right].toLowerCase()+'</b>. Остальные числа уже даны.'};
}

function taskBase(){
 const kind=rnd(0,2);
 if(kind===0){
  const a=pick([200,400,500,600,800]),k=pick([1.15,1.2,1.25,1.4]),b=r2(a*k);
  const mix=shuffle([{t:a+' ₽ — цена до изменения',w:null},
   {t:fmt(b)+' ₽ — цена после изменения',w:'Проценты изменения всегда считают от <b>исходного</b> значения, а не от нового.'},
   {t:fmt(b-a)+' ₽ — величина прироста',w:'Это то, на что цена изменилась. Целым служит не прирост, а то, к чему его прибавили.'},
   {t:'100 ₽',w:'Числа 100 в условии нет. Сто — это количество процентов, а не рублей.'}]);
  return {type:'choice',prompt:'Цена выросла с <b>'+a+'</b> ₽ до <b>'+fmt(b)+'</b> ₽.<br><b>Что здесь принято за 100%?</b>',
   options:mix.map(o=>o.t),answer:mix.findIndex(o=>!o.w),
   wrong:Object.fromEntries(mix.map((o,i)=>[i,o.w]).filter(x=>x[1])),
   hint1:'Спросите: рост произошёл <b>от чего</b>? Это «от чего» и есть 100%.',
   hint2:'Изменение всегда отсчитывают от того, что было раньше.',
   solution:'Рост шёл от старой цены, поэтому 100% — это <b>'+a+' ₽</b>. Прирост '+fmt(b-a)+' ₽ сравнивают именно с ней.'};
 }
 if(kind===1){
  let w,p,v;
  do{w=pick([400,500,600,800]);p=pick([20,25,40,60]);v=r2(w*p/100)}
  while(v===100||v===p||v===w||p===w||w===100);
  const mix=shuffle([{t:'Число '+w,w:null},
   {t:'Число '+fmt(v),w:'Это часть — те самые '+p+'%. Целое больше неё.'},
   {t:'Число '+p,w:'Это количество процентов, а не величина целого.'},
   {t:'Число 100',w:'Сто здесь — только количество процентов в целом.'}]);
  return {type:'choice',prompt:'<b>'+p+'%</b> числа <b>'+w+'</b> равны <b>'+fmt(v)+'</b>.<br><b>Что здесь принято за 100%?</b>',
   options:mix.map(o=>o.t),answer:mix.findIndex(o=>!o.w),
   wrong:Object.fromEntries(mix.map((o,i)=>[i,o.w]).filter(x=>x[1])),
   hint1:'Процент всегда берут <b>от</b> какого-то числа. Найдите в условии слово «от».',
   hint2:'Целое — то число, часть которого посчитали.',
   solution:'Проценты взяли от числа '+w+', значит 100% — это <b>'+w+'</b>.'};
 }
 const t=pick([25,40,50,60,80]),p=pick([5,8,12,15,20]);
 const mix=shuffle([{t:'Все '+t+' — весь класс',w:null},
  {t:'Только '+p+' — те, кто опоздал',w:'Это часть. Её как раз и выделили из целого.'},
  {t:'Разность '+(t-p),w:'Это остальные ученики — тоже часть, а не целое.'},
  {t:'Число 100',w:'В условии нет ста учеников. Сто — количество процентов.'}]);
 return {type:'choice',prompt:'В классе <b>'+t+'</b> '+plural(t,'ученик','ученика','учеников')+', <b>'+p+'</b> из них опоздали.<br><b>Что здесь принято за 100%?</b>',
  options:mix.map(o=>o.t),answer:mix.findIndex(o=>!o.w),
  wrong:Object.fromEntries(mix.map((o,i)=>[i,o.w]).filter(x=>x[1])),
  hint1:'Опоздавших выделили из какой группы? Она и есть целое.',
  hint2:'Целое больше любой своей части.',
  solution:'Опоздавших выделили из всего класса, поэтому 100% — это <b>'+t+'</b>.'};
}

function taskEstimate(){
 const kind=rnd(0,3);
 const YES='Больше',NO='Меньше',EQ='Ровно столько же';
 if(kind===0){
  const p=pick([20,25,40,60,80]),v=pick([120,180,240,300]);
  return {type:'choice',prompt:'<b>'+p+'%</b> числа равны <b>'+v+'</b>. Само число будет больше или меньше '+v+'?<br><span style="color:var(--soft);font-weight:600">Считать не нужно.</span>',
   options:[YES,NO,EQ],answer:0,
   wrong:{1:'Часть не может быть больше целого. Раз '+v+' — это только '+p+'%, целое обязано быть больше.',
          2:'Равенство было бы только при 100%, а здесь '+p+'%.'},
   hint1:'Число '+v+' — это лишь часть целого, причём не всё целое.',
   hint2:'Целое всегда больше своей части.',
   solution:'<b>Больше.</b> '+v+' — это только '+p+'% целого, значит целое больше. Это и есть проверка ответа: если получилось меньше '+v+', где-то ошибка.'};
 }
 if(kind===1){
  const n=pick([400,600,800,1200]),p=pick([10,15,20,25]);
  return {type:'choice',prompt:'Цену <b>'+n+'</b> ₽ снизили на <b>'+p+'%</b>. Новая цена больше или меньше '+n+' ₽?<br><span style="color:var(--soft);font-weight:600">Считать не нужно.</span>',
   options:[YES,NO,EQ],answer:1,
   wrong:{0:'Снижение уменьшает величину. Больше исходного она стать не может.',
          2:'Ровно столько же было бы при снижении на 0%.'},
   hint1:'Слово «снизили» задаёт направление.',
   hint2:'Уменьшение всегда даёт число меньше исходного.',
   solution:'<b>Меньше.</b> Снижение на '+p+'% отнимает часть от '+n+' ₽, поэтому результат меньше.'};
 }
 if(kind===2){
  const n=pick([200,400,500,600]),p=pick([110,120,140,150,180]);
  return {type:'choice',prompt:'Новое значение составляет <b>'+p+'%</b> от старого, равного <b>'+n+'</b>. Новое больше или меньше старого?<br><span style="color:var(--soft);font-weight:600">Считать не нужно.</span>',
   options:[YES,NO,EQ],answer:0,
   wrong:{1:'Больше ста процентов — это больше целого.',
          2:'Ровно столько же было бы при 100%, а здесь '+p+'%.'},
   hint1:'Сто процентов — это всё старое значение целиком.',
   hint2:'Если процентов больше ста, величина выросла.',
   solution:'<b>Больше.</b> 100% — это само старое значение, а '+p+'% больше ста, значит величина выросла на '+(p-100)+'%.'};
 }
 const n=pick([500,800,1000,1200]),p=pick([10,20,25]);
 return {type:'choice',prompt:'Число <b>'+n+'</b> увеличили на <b>'+p+'%</b>, а результат уменьшили на <b>'+p+'%</b>. Итог больше, меньше или равен '+n+'?<br><span style="color:var(--soft);font-weight:600">Считать не нужно.</span>',
  options:[YES,NO,EQ],answer:1,
  wrong:{0:'Прибавили '+p+'% от '+n+', а вычли '+p+'% уже от большего числа. Вычли больше, чем прибавили.',
         2:'Проценты брали от разных чисел, поэтому они не гасят друг друга.'},
  hint1:'От какого числа считали прибавку? А от какого — вычитание?',
  hint2:'Второй раз процент брали от уже увеличенного числа, то есть от большего.',
  solution:'<b>Меньше.</b> Прибавка — '+p+'% от '+n+', а вычитание — '+p+'% от числа побольше. Вычли больше, чем прибавили, поэтому итог ниже исходного.'};
}

