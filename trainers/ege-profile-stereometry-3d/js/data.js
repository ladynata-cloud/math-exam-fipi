function makeSprite(text, { fontPx=52, color="#1D2434", bg=null, pad=14, italicSerif=true } = {}) {
  const cvs = document.createElement("canvas");
  const ctx = cvs.getContext("2d");
  const font = `${italicSerif?"italic ":""}${fontPx}px Georgia, serif`;
  ctx.font = font;
  const w = Math.ceil(ctx.measureText(text).width) + pad*2, h = fontPx + pad*2;
  cvs.width = w*2; cvs.height = h*2;
  const c2 = cvs.getContext("2d"); c2.scale(2,2);
  if (bg) {
    c2.fillStyle = bg; const r = 10;
    c2.beginPath();
    c2.moveTo(r,0); c2.arcTo(w,0,w,h,r); c2.arcTo(w,h,0,h,r); c2.arcTo(0,h,0,0,r); c2.arcTo(0,0,w,0,r);
    c2.fill();
  }
  c2.font = font; c2.fillStyle = color; c2.textAlign="center"; c2.textBaseline="middle";
  c2.fillText(text, w/2, h/2 + 2);
  const tex = new THREE.CanvasTexture(cvs); tex.minFilter = THREE.LinearFilter;
  const sp = new THREE.Sprite(new THREE.SpriteMaterial({ map:tex, transparent:true, depthTest:false }));
  const k = 0.0075;
  sp.scale.set(w*k, h*k, 1);
  sp.renderOrder = 10;
  return sp;
}

