function taskPart(){
 let n,p,a;
 do{n=pick([120,150,200,240,250,300,320,350,400,450,500,600,700,800]);
    p=pick([4,5,8,10,12,15,20,25,30,35,40,60,75,80]);a=r2(n*p/100)}
 while(!(Number.isInteger(a*10) && a!==n && a!==p && a>0));
 const v=rnd(0,2);
 const prompt=v===0?'Найдите <b>'+p+'%</b> от числа <b>'+n+'</b>.'
  :v===1?'Товар стоит <b>'+n+'</b> ₽. Скидка <b>'+p+'%</b>. На сколько рублей снизится цена?'
  :'В городе <b>'+n+'</b> тысяч жителей, <b>'+p+'%</b> из них моложе тридцати. Сколько это тысяч?';
 return {type:'input',prompt:prompt,answer:a,
  traps:[{v:r2(n*100/p),msg:'Вы решили обратную задачу: нашли число, для которого '+n+' было бы '+p+'%. Здесь целое уже дано — это '+n+'.'},
         {v:r2(n-a),msg:'Это остаток после вычитания процента. Спрашивают сам процент, а не то, что осталось.'},
         {v:p,msg:'Это количество процентов из условия. Нужно их значение от числа '+n+'.'},
         {v:r2(n/100),msg:'Это только 1%. Умножьте его на '+p+'.'}],
  hint1:'Сначала найдите 1%: '+n+' : 100 = '+fmt(n/100)+'.',
  hint2:'Теперь умножьте 1% на '+p+': '+fmt(n/100)+' · '+p+'.',
  solution:'1% = '+n+' : 100 = '+fmt(n/100)+'. Тогда '+p+'% = '+fmt(n/100)+' · '+p+' = <b>'+fmt(a)+'</b>.'};
}

function taskWhole(){
 let w,p,v;
 do{w=pick([200,240,300,320,400,450,500,600,700,750,800,900,1200,1500]);
    p=pick([5,8,10,12,15,20,25,30,40,50,60,75,80]);v=r2(w*p/100)}
 while(!(Number.isInteger(v) && v!==w && p!==100 && v>0));
 const k=rnd(0,2);
 const prompt=k===0?'<b>'+p+'%</b> некоторого числа равны <b>'+v+'</b>. Найдите это число.'
  :k===1?'Скидка составила <b>'+p+'%</b>, то есть <b>'+v+'</b> ₽. Какой была цена до скидки?'
  :'Известно, что <b>'+v+'</b> — это <b>'+p+'%</b> от задуманного числа. Чему равно число?';
 return {type:'input',prompt:prompt,answer:w,
  traps:[{v:r2(v*p/100),msg:'Вы нашли '+p+'% от '+v+'. Но '+v+' — это уже часть; целое, наоборот, больше.'},
         {v:r2(v*100),msg:'Умножать на 100 нужно после деления на число процентов, а не вместо него.'},
         {v:r2(w-v),msg:'Это остальная часть числа. Спрашивают всё число целиком.'},
         {v:r2(v/p),msg:'Вы нашли сотую долю целого, то есть 1%. Его ещё нужно умножить на 100.'}],
  hint1:'Если '+p+'% — это '+v+', то 1% в '+p+' раз меньше: '+v+' : '+p+' = '+fmt(v/p)+'.',
  hint2:'Целое — это 100 таких долей: '+fmt(v/p)+' · 100.',
  solution:'1% = '+v+' : '+p+' = '+fmt(v/p)+'. Всё число: '+fmt(v/p)+' · 100 = <b>'+w+'</b>. Проверка: '+p+'% от '+w+' действительно '+v+'.'};
}

function taskPercent(){
 let tot,p,part;
 do{tot=pick([20,25,40,50,60,80,120,150,160,200,240,250,300,400,500]);
    p=pick([4,5,8,10,12,15,20,24,25,30,36,40,50,60,64,75,80]);part=r2(tot*p/100)}
 while(!(Number.isInteger(part) && part!==tot && part>0 && p!==100 && p!==part));
 const k=rnd(0,2);
 const prompt=k===0?'Сколько процентов составляет <b>'+part+'</b> от <b>'+tot+'</b>?'
  :k===1?'В зале <b>'+tot+'</b> мест, занято <b>'+part+'</b>. Какой процент мест занят?'
  :'Число <b>'+part+'</b> — какую часть числа <b>'+tot+'</b> оно составляет в процентах?';
 return {type:'input',prompt:prompt,answer:p,unit:'%',
  traps:[{v:r2(100-p),msg:'Это процент оставшихся. Спрашивают про названную часть.'},
         {v:r2(tot/part*100),msg:'Дробь перевёрнута: сверху часть ('+part+'), снизу целое ('+tot+').'},
         {v:r2(part/tot),msg:'Это доля, её осталось умножить на 100, чтобы получить проценты.'},
         {v:r2(tot-part),msg:'Это разность в штуках, а не процент.'}],
  hint1:'Разделите часть на целое: '+part+' : '+tot+' = '+fmt(part/tot)+'.',
  hint2:'Долю переведите в проценты умножением на 100.',
  solution:part+' : '+tot+' = '+fmt(part/tot)+', и '+fmt(part/tot)+' · 100 = <b>'+p+'%</b>.'};
}

function makeTask(skill){return {part:taskPart,whole:taskWhole,percent:taskPercent}[skill]()}

