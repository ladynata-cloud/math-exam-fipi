function taskBase(){
 const a=pick([200,400,500,600,800]),k=pick([1.15,1.2,1.25,1.4]),b=r2(a*k);
 const mix=shuffle([{t:a+' ₽ — цена до изменения',w:null},
  {t:fmt(b)+' ₽ — цена после изменения',w:'Процент изменения считают от <b>исходного</b> значения.'},
  {t:fmt(b-a)+' ₽ — величина прироста',w:'Это то, на что цена изменилась, а не то, с чем её сравнивают.'},
  {t:'100 ₽',w:'Числа 100 в условии нет: сто — это количество процентов.'}]);
 return {type:'choice',prompt:'Цена выросла с <b>'+a+'</b> ₽ до <b>'+fmt(b)+'</b> ₽. <b>Что здесь принято за 100%?</b>',
  options:mix.map(o=>o.t),answer:mix.findIndex(o=>!o.w),
  wrong:Object.fromEntries(mix.map((o,i)=>[i,o.w]).filter(x=>x[1])),
  hint1:'Рост произошёл от чего? Это и есть целое.',hint2:'Изменение отсчитывают от того, что было раньше.',
  solution:'Рост шёл от старой цены, поэтому 100% — это <b>'+a+' ₽</b>.'};
}
function taskPart(){
 let n,p,a;
 do{n=pick([120,200,240,300,400,450,500,600,800]);p=pick([5,8,10,12,15,20,25,30,40,60,75]);a=r2(n*p/100)}
 while(!(Number.isInteger(a*10)&&a>0&&a!==n&&a!==p));
 return {type:'input',prompt:'Найдите <b>'+p+'%</b> от числа <b>'+n+'</b>.',answer:a,
  traps:[{v:r2(n*100/p),msg:'Это обратная задача. Здесь целое дано — оно равно '+n+'.'},
         {v:r2(n-a),msg:'Это остаток, а не сам процент.'},
         {v:r2(n/100),msg:'Это 1%. Умножьте его на '+p+'.'}],
  hint1:'1% = '+n+' : 100 = '+fmt(n/100)+'.',hint2:'Умножьте 1% на '+p+'.',
  solution:'1% = '+fmt(n/100)+', значит '+p+'% = '+fmt(n/100)+' · '+p+' = <b>'+fmt(a)+'</b>.'};
}
function taskWhole(){
 let w,p,v;
 do{w=pick([200,300,400,500,600,800,900,1200]);p=pick([5,10,12,15,20,25,30,40,50,60,75]);v=r2(w*p/100)}
 while(!(Number.isInteger(v)&&v!==w&&v>0));
 return {type:'input',prompt:'<b>'+p+'%</b> некоторого числа равны <b>'+v+'</b>. Найдите это число.',answer:w,
  traps:[{v:r2(v*p/100),msg:'Вы нашли '+p+'% от '+v+'. Но '+v+' — уже часть, целое больше.'},
         {v:r2(v*100),msg:'Умножать на 100 нужно после деления на '+p+'.'},
         {v:r2(v/p),msg:'Это 1%. Целое — сто таких долей.'}],
  hint1:'1% = '+v+' : '+p+' = '+fmt(v/p)+'.',hint2:'Целое — это сто таких долей.',
  solution:'1% = '+fmt(v/p)+', всё число: '+fmt(v/p)+' · 100 = <b>'+w+'</b>.'};
}
function taskPercent(){
 let tot,p,part;
 do{tot=pick([20,25,40,50,60,80,120,150,200,250,400]);p=pick([4,5,8,10,12,15,20,25,30,40,50,60,75,80]);part=r2(tot*p/100)}
 while(!(Number.isInteger(part)&&part!==tot&&part>0&&p!==part));
 return {type:'input',prompt:'Сколько процентов составляет <b>'+part+'</b> от <b>'+tot+'</b>?',answer:p,unit:'%',
  traps:[{v:r2(100-p),msg:'Это процент оставшейся части.'},
         {v:r2(tot/part*100),msg:'Дробь перевёрнута: сверху часть, снизу целое.'},
         {v:r2(part/tot),msg:'Это доля. Умножьте её на 100.'}],
  hint1:'Разделите '+part+' на '+tot+'.',hint2:'Долю переведите в проценты умножением на 100.',
  solution:part+' : '+tot+' = '+fmt(part/tot)+', то есть <b>'+p+'%</b>.'};
}
function taskApply(){
 let n,p,up,a;
 do{n=pick([200,300,400,500,600,800,1200,1500,2000]);p=pick([5,10,15,20,25,30,40,50,75]);
    up=Math.random()<.5;a=r2(n*(100+(up?p:-p))/100)}
 while(!(Number.isInteger(a)&&a>0&&a!==n));
 return {type:'input',prompt:'Цена <b>'+n+'</b> ₽ '+(up?'выросла':'снизилась')+' на <b>'+p+'%</b>. Какой она стала?',answer:a,
  traps:[{v:r2(n*p/100),msg:'Это размер '+(up?'надбавки':'скидки')+'. Его нужно ещё '+(up?'прибавить к':'вычесть из')+' '+n+'.'},
         {v:r2(n*(100-(up?p:-p))/100),msg:'Направление перепутано.'},
         {v:r2(up?n+p:n-p),msg:'Проценты нельзя прибавлять как рубли: '+p+'% от '+n+' — это '+fmt(n*p/100)+'.'}],
  hint1:''+p+'% от '+n+' = '+fmt(n*p/100)+'.',hint2:(up?'Прибавьте':'Вычтите')+' найденное к '+n+'.',
  solution:''+n+' · '+fmt((100+(up?p:-p))/100)+' = <b>'+fmt(a)+'</b>.'};
}
function taskChange(){
 let a,p,up,b;
 do{a=pick([200,250,300,400,500,600,800,1000,1200]);p=pick([5,10,15,20,25,30,40,50,60]);
    up=Math.random()<.5;b=r2(a*(100+(up?p:-p))/100)}
 while(!(Number.isInteger(b)&&b>0&&b!==a));
 const d=r2(Math.abs(b-a));
 return {type:'input',prompt:'Значение изменилось с <b>'+a+'</b> до <b>'+fmt(b)+'</b>. На сколько процентов оно '+(up?'выросло':'уменьшилось')+'?',
  answer:p,unit:'%',
  traps:[{v:r2(d/b*100),msg:'Вы разделили на новое значение. Считать нужно от исходного — от '+a+'.'},
         {v:d,msg:'Это изменение в единицах, а не в процентах.'},
         {v:r2(b/a*100),msg:'Это сколько <b>стало</b> процентов. Спрашивают, на сколько изменилось: вычтите 100.'}],
  hint1:'Изменение равно '+fmt(d)+'.',hint2:'Разделите '+fmt(d)+' на '+a+' и умножьте на 100.',
  solution:fmt(d)+' : '+a+' · 100 = <b>'+p+'%</b>. Делим на исходное значение.'};
}
function taskSuccessive(){
 let n,p,q,d1,d2,mid,a;
 do{n=pick([500,800,1000,1200,2000]);p=pick([10,20,25,50]);q=pick([10,20,25,50]);
    d1=Math.random()<.5?1:-1;d2=Math.random()<.5?1:-1;
    mid=r2(n*(100+d1*p)/100);a=r2(mid*(100+d2*q)/100)}
 while(!(Number.isInteger(mid)&&Number.isInteger(a)&&a>0&&a!==n));
 const w=d=>d>0?'увеличили':'уменьшили';
 const traps=[{v:r2(n*(100+d1*p+d2*q)/100),msg:'Проценты сложены. Второй процент берут от '+fmt(mid)+', а не от '+n+'.'},
              {v:mid,msg:'Это результат только первого изменения.'}];
 if(d1*d2<0&&p===q)traps.push({v:n,msg:'Изменения не гасят друг друга: сначала процент брали от '+n+', потом от '+fmt(mid)+'.'});
 return {type:'input',prompt:'Число <b>'+n+'</b> сначала '+w(d1)+' на <b>'+p+'%</b>, затем результат '+w(d2)+' на <b>'+q+'%</b>. Найдите итог.',
  answer:a,traps:traps,
  hint1:'Первый шаг даёт '+fmt(mid)+'.',hint2:'Второй процент берите от '+fmt(mid)+'.',
  solution:''+n+' · '+fmt((100+d1*p)/100)+' = '+fmt(mid)+', затем '+fmt(mid)+' · '+fmt((100+d2*q)/100)+' = <b>'+fmt(a)+'</b>.'};
}
function taskProp(){
 if(Math.random()<.5){
  let a,b,k;do{a=rnd(2,12);b=rnd(2,12);k=rnd(2,9)}while(a===b);
  const c=a*k,d=b*k,val=d;
  return {type:'input',prompt:'Найдите неизвестный член пропорции:<br><span style="font-size:20px">'+a+' : '+b+' = '+c+' : <b>x</b></span>',
   answer:val,
   traps:[{v:r2(a*c/b),msg:'Крайние здесь — '+a+' и <b>x</b>, средние — '+b+' и '+c+'.'},
          {v:r2(a*b/c),msg:'Проверьте порядок: x = '+b+' · '+c+' : '+a+'.'}],
   hint1:'Произведение крайних равно произведению средних.',hint2:'x = '+b+' · '+c+' : '+a+'.',
   solution:''+a+' · x = '+b+' · '+c+', значит x = '+b+' · '+c+' : '+a+' = <b>'+val+'</b>.'};
 }
 let n,p,a;
 do{n=pick([200,300,400,500,600,800]);p=pick([5,10,15,20,25,30,40,60,75]);a=r2(n*p/100)}
 while(!Number.isInteger(a)||a===n);
 return {type:'input',prompt:'По схеме «целое : часть = 100 : процент» найдите <b>'+p+'%</b> от <b>'+n+'</b>.',answer:a,
  traps:[{v:r2(n*100/p),msg:'Пропорция перевёрнута: '+n+' отвечает ста процентам.'},
         {v:p,msg:'Это количество процентов, а не их значение.'}],
  hint1:''+n+' : x = 100 : '+p+'.',hint2:'Крест-накрест: x = '+n+' · '+p+' : 100.',
  solution:''+n+' : x = 100 : '+p+', откуда x = '+n+' · '+p+' : 100 = <b>'+fmt(a)+'</b>.'};
}

function makeTask(skill){return {base:taskBase,part:taskPart,whole:taskWhole,percent:taskPercent,
 apply:taskApply,change:taskChange,successive:taskSuccessive,prop:taskProp}[skill]()}

