function taskApply(){
 let n,p,up,a;
 do{n=pick([200,240,300,400,500,600,800,900,1200,1500,2000]);p=pick([5,10,15,20,25,30,40,50,60,75]);
    up=Math.random()<.5;a=r2(n*(100+(up?p:-p))/100)}
 while(!(Number.isInteger(a) && a>0 && a!==n));
 const k=rnd(0,2);
 const word=up?'увеличили':'уменьшили';
 const prompt=k===0?'Число <b>'+n+'</b> '+word+' на <b>'+p+'%</b>. Найдите результат.'
  :k===1?(up?'Цена <b>'+n+'</b> ₽ выросла на <b>'+p+'%</b>. Какой она стала?'
            :'Цена <b>'+n+'</b> ₽ снизилась на <b>'+p+'%</b>. Какой она стала?')
  :'Величина была равна <b>'+n+'</b> и изменилась на '+(up?'+':'−')+'<b>'+p+'%</b>. Чему она равна теперь?';
 return {type:'input',prompt:prompt,answer:a,
  traps:[{v:r2(n*p/100),msg:'Это сам процент — то, <b>на сколько</b> изменилось число. Его нужно ещё '+(up?'прибавить к':'вычесть из')+' '+n+'.'},
         {v:r2(n*(100-(up?p:-p))/100),msg:'Направление перепутано: в условии величину '+word+', а вы сделали наоборот.'},
         {v:r2(up?n+p:n-p),msg:'Проценты нельзя прибавлять как рубли. Сначала найдите '+p+'% от '+n+', и уже это число прибавляйте.'},
         {v:n,msg:'Число не изменилось. Но '+p+'% от '+n+' — это '+fmt(n*p/100)+', и на столько оно должно сдвинуться.'}],
  hint1:''+p+'% от '+n+' — это '+n+' · '+p+' : 100 = '+fmt(n*p/100)+'.',
  hint2:'Теперь '+(up?'прибавьте':'вычтите')+': '+n+' '+(up?'+':'−')+' '+fmt(n*p/100)+'.',
  solution:''+p+'% от '+n+' равны '+fmt(n*p/100)+'. Значит результат: '+n+' '+(up?'+':'−')+' '+fmt(n*p/100)+' = <b>'+fmt(a)+'</b>.'
   +' Короче: '+n+' · '+fmt((100+(up?p:-p))/100)+' = '+fmt(a)+'.'};
}

function taskChange(){
 let a,p,up,b;
 do{a=pick([200,250,300,400,500,600,800,1000,1200,1500,2000]);p=pick([5,10,15,20,25,30,40,50,60,75]);
    up=Math.random()<.5;b=r2(a*(100+(up?p:-p))/100)}
 while(!(Number.isInteger(b) && b>0 && b!==a));
 const d=r2(Math.abs(b-a));
 const k=rnd(0,2);
 const prompt=k===0?'Значение изменилось с <b>'+a+'</b> до <b>'+fmt(b)+'</b>. На сколько процентов оно '+(up?'выросло':'уменьшилось')+'?'
  :k===1?'Цена была <b>'+a+'</b> ₽, стала <b>'+fmt(b)+'</b> ₽. На сколько процентов она '+(up?'подорожала':'подешевела')+'?'
  :'Показатель '+(up?'вырос':'упал')+' с <b>'+a+'</b> до <b>'+fmt(b)+'</b>. Найдите процент изменения.';
 return {type:'input',prompt:prompt,answer:p,unit:'%',
  traps:[{v:r2(d/b*100),msg:'Вы разделили на новое значение. Процент изменения считают от <b>исходного</b> — от '+a+'.'},
         {v:d,msg:'Это величина изменения в единицах, а не в процентах. Её надо сравнить с '+a+'.'},
         {v:r2(b/a*100),msg:'Это доля нового значения от старого — сколько стало процентов. Спрашивают, на сколько <b>изменилось</b>: вычтите 100.'},
         {v:r2(100-p),msg:'Похоже, вы посчитали, сколько процентов осталось, а не на сколько изменилось.'}],
  hint1:'Сначала найдите изменение: |'+fmt(b)+' − '+a+'| = '+fmt(d)+'.',
  hint2:'Теперь сравните изменение с исходным значением: '+fmt(d)+' : '+a+' · 100.',
  solution:'Изменение: '+fmt(d)+'. Сравниваем с исходным: '+fmt(d)+' : '+a+' = '+fmt(d/a)+', то есть <b>'+p+'%</b>.'
   +' Делить нужно именно на '+a+' — на то, что было до изменения.'};
}

function taskMeaning(){
 const back=Math.random()<.5;
 const p=pick([110,115,120,125,140,150,160,180,90,85,80,75,60,45]);
 const diff=Math.abs(p-100),up=p>100;
 if(!back){
  return {type:'input',prompt:'Новое значение составляет <b>'+p+'%</b> от старого. На сколько процентов оно '+(up?'выросло':'уменьшилось')+'?',
   answer:diff,unit:'%',
   traps:[{v:p,msg:'Это сколько <b>стало</b> — вся новая величина в процентах от старой. Изменение — только то, что сверх ста'+(up?'':' или недостаёт до ста')+'.'},
          {v:r2(100+p),msg:'Сто процентов — это само старое значение, его не нужно добавлять ещё раз.'},
          {v:100,msg:'Сто процентов — это старое значение целиком, а не величина изменения.'}],
   hint1:'Старое значение — это ровно 100%.',
   hint2:'Сравните '+p+'% со ста: разница и есть изменение.',
   solution:'Старое значение — 100%. Новое — '+p+'%. Разница '+p+' − 100 = <b>'+diff+'%</b>'
    +(up?' — на столько выросло.':' — на столько уменьшилось (знак минус в ответе не пишем, направление уже сказано словом).')};
 }
 return {type:'input',prompt:'Величина '+(up?'выросла':'уменьшилась')+' на <b>'+diff+'%</b>. Сколько процентов от старого значения составляет новое?',
  answer:p,unit:'%',
  traps:[{v:diff,msg:'Это только изменение. Вопрос про всю новую величину: к ста процентам нужно '+(up?'прибавить':'вычесть')+' изменение.'},
         {v:up?r2(100-diff):r2(100+diff),msg:'Направление перепутано: величина '+(up?'выросла':'уменьшилась')+'.'},
         {v:100,msg:'Сто процентов — это старое значение. Новое отличается от него на '+diff+'%.'}],
  hint1:'Старое значение — это 100%.',
  hint2:'К ста процентам '+(up?'прибавьте':'вычтите')+' '+diff+'.',
  solution:'100% '+(up?'+':'−')+' '+diff+'% = <b>'+p+'%</b>. Новая величина составляет '+p+'% от старой.'};
}

function taskSuccessive(){
 let n,p,q,d1,d2,mid,a;
 do{n=pick([500,800,1000,1200,1500,2000,2500]);p=pick([10,20,25,50]);q=pick([10,20,25,50]);
    d1=Math.random()<.5?1:-1;d2=Math.random()<.5?1:-1;
    mid=r2(n*(100+d1*p)/100);a=r2(mid*(100+d2*q)/100)}
 while(!(Number.isInteger(mid) && Number.isInteger(a) && a>0 && a!==n));
 const w=d=>d>0?'увеличили':'уменьшили';
 const naive=r2(n*(100+d1*p+d2*q)/100);
 const traps=[{v:naive,msg:'Проценты сложены, а так нельзя: второй процент берут уже от изменённого числа '+fmt(mid)+', а не от '+n+'.'},
              {v:mid,msg:'Это результат только первого изменения. Второе ещё не выполнено.'}];
 if(d1*d2<0 && p===q)traps.push({v:n,msg:'Кажется, что изменения гасят друг друга, но нет: '+p+'% брали от '+n+', а потом от '+fmt(mid)+'. Проценты одинаковые, а числа разные.'});
 return {type:'input',prompt:'Число <b>'+n+'</b> сначала '+w(d1)+' на <b>'+p+'%</b>, затем результат '+w(d2)+' на <b>'+q+'%</b>. Чему равно итоговое число?',
  answer:a,traps:traps,
  hint1:'Сделайте первое изменение: '+n+' '+(d1>0?'+':'−')+' '+p+'% = '+fmt(mid)+'.',
  hint2:'Второй процент берите уже от '+fmt(mid)+', а не от '+n+'.',
  solution:'Шаг 1: '+p+'% от '+n+' — это '+fmt(n*p/100)+', получаем '+fmt(mid)+'.<br>'
   +'Шаг 2: '+q+'% уже от '+fmt(mid)+' — это '+fmt(mid*q/100)+', получаем <b>'+fmt(a)+'</b>.<br>'
   +'Короче: '+n+' · '+fmt((100+d1*p)/100)+' · '+fmt((100+d2*q)/100)+' = '+fmt(a)+'.'};
}

function makeTask(skill){return {apply:taskApply,change:taskChange,meaning:taskMeaning,successive:taskSuccessive}[skill]()}

